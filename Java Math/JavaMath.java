class JavaMath
{
    public static void main(String[] args)
    {
        System.out.println(Math.max(5,7)); //identifies the maximum value using math class
        System.out.println(Math.min(4,9)); //identifies the minimum value using math class
        System.out.println(Math.sqrt(64)); //identifies the square root of value in float type using math class
        System.out.println(Math.abs(-7.4)); //identifies the absolute value using math class
        System.out.println(Math.random()); //identifies the random float value using math class
        int mul = (int)(Math.random()*10); //identifies the random float value using math class & multiply by 10
        System.out.println(mul); //print the value inside a variable
    }
}