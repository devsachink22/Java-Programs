class Arrays1
{
    public static void main(String[] args)
    {
        String[] name = {"Sachin","Balveer","Jatin","Mohit"}; //initialization of array
        System.out.println(name.length); //length of array
        for (int i = 0; i<name.length; i++) //iteration of array
        {
            System.out.println(name[i]); //print the elements of array
        }
    }
}