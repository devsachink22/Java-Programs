/*If-Else using ternary operators*/
class TernaryOperators
{
    public static void main(String[] args)
    {
        int i=16;
        // variable = (condtitions) ? expressionTrue : expressionFalse;
        String s = (i>0) ? "Given number is a positive number." : "Given number is a negative number.";
        System.out.println(s); //print an expression as per the defined condition
    }
}